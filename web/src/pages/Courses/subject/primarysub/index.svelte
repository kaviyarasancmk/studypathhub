      <li><strong>Mathematics (MATH):</strong> The study of numbers, operations, and problem-solving.</li>
      <li><strong>Science (SCI):</strong> Introduction to basic scientific concepts, including biology, chemistry, and physics.</li>
      <li><strong>Social Studies (SOC):</strong> A broad subject covering history, geography, and civics.</li>
      <li><strong>Tamil (TAM):</strong> The primary language subject in Tamil Nadu, focusing on the Tamil language and literature.</li>
      <li><strong>Hindi (HIN):</strong> Studied in many regions, Hindi is an additional language subject.</li>
      <li><strong>Regional Language (RL):</strong> In regions where Hindi is not the primary language, a regional language is taught.</li>
      <li><strong>Environmental Studies (EVS):</strong> An interdisciplinary subject covering topics related to the environment, natural resources, and local ecosystems.</li>
      <li><strong>General Knowledge (GK):</strong> Introduction to general knowledge, including current affairs and basic awareness.</li>
      <li><strong>Art and Craft (ART):</strong> Creative subjects that include drawing, painting, and crafts.</li>
      <li><strong>Physical Education (PE):</strong> Involves physical activities, sports, and health education.</li>
      <li><strong>Moral Science (MOR):</strong> Fosters moral and ethical values in students.</li>
    </ul>
  
